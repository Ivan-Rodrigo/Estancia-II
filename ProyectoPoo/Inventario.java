
public class Inventario{
    private int totalMotos;
    private int totalAccesorios;
    private int totalCascos;
    private int totalChamarras;
    private int totalLuces;
    
    public Inventario(){

    }

    public int getTotalMotos() {
        return totalMotos;
    }

    public void setTotalMotos(int totalMotos) {
        this.totalMotos = totalMotos;
    }

    public int getTotalAccesorios() {
        return totalAccesorios;
    }

    public void setTotalAccesorios(int totalAccesorios) {
        this.totalAccesorios = totalAccesorios;
    }

    public int getTotalCascos() {
        return totalCascos;
    }

    public void setTotalCascos(int totalCascos) {
        this.totalCascos = totalCascos;
    }

    public int getTotalChamarras() {
        return totalChamarras;
    }

    public void setTotalChamarras(int totalChamarras) {
        this.totalChamarras = totalChamarras;
    }

    public int getTotalLuces() {
        return totalLuces;
    }

    public void setTotalLuces(int totalLuces) {
        this.totalLuces = totalLuces;
    }
    
    
}